package es.dg.avan.jtools.exceptions;

public class AvanToolException  extends Exception {

	/**
	 * Generated Serial version
	 */
	private static final long serialVersionUID = 1493920538853252937L;

	public AvanToolException() {
		super();
	}

	public AvanToolException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AvanToolException(String message, Throwable cause) {
		super(message, cause);
	}

	public AvanToolException(String message) {
		super(message);
	}

	public AvanToolException(Throwable cause) {
		super(cause);
	}
	

}

